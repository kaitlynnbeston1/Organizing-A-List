# Creating list and printing.
places = ["tokyo", "berlin", "orlando", "london", "oslo", "helsinki"]
print(places)


# using the sorted function
print(sorted(places))
print(places)
print(sorted(places, reverse=True))
print(places)


# Using the reverse function.
places.reverse()
print(places)
places.reverse()
print(places)


# Using sort function
places.sort()
print(places)
print(places.sort(reverse=True))
print(places)